// Helper functions
export function isBitmapFont(family: string): boolean {
  return bitmapFonts.some(f => f.family === family);
}

export function getBitmapFont(family: string): BitmapFont | undefined {
  return bitmapFonts.find(f => f.family === family);
}

export function getBitmapFontSizes(family: string): number[] {
  const font = getBitmapFont(family);
  return font?.sizes.map(s => s.height) ?? [];
}

export function hasBitmapSize(family: string, size: number): boolean {
  const font = getBitmapFont(family);
  return font?.sizes.some(s => s.height === size) ?? false;
}
